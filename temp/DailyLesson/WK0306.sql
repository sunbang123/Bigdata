SELECT EMPNO AS 사원번호,ENAME AS 사원이름 -- AS 생략 가능.
FROM EMP;

--사원테이블에서 직무가 SALES인 사원의 정보를 출력하라?
SELECT *
FROM EMP
WHERE JOB='SALESMAN';

SELECT * FROM EMP;
--사원테이블에서 SAL이 1000-2000사이인 사원을 출력하라?
SELECT *
FROM EMP
WHERE SAL NOT BETWEEN 1000 AND 2000;

--사원테이블에서 직무가 점원이거나 영업사원인 경우를 출력하라?
SELECT *
FROM EMP
WHERE JOB NOT IN('CLERK','SALESMAN');

