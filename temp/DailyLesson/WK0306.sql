--사원테이블에서 직무별로 평균급여를 출력하라?
SELECT DEPTNO,ROUND(STDDEV(SAL),1)
FROM EMP
GROUP BY DEPTNO -- group 함수가 쓰여짐으로써...
ORDER BY 2; -- 오름차순!

SELECT MAX(SAL),MIN(SAL),MEDIAN(SAL),AVG(SAL),STDDEV(SAL)
ROUND(STDDEV(SAL))
FROM EMP;

--사원테이블에서 직무별 평균급여가 2000이상인 사원들의 정보를 출력하라
SELECT JOB,ROUND(AVG(SAL),1)
FROM EMP
GROUP BY JOB
HAVING AVG(SAL)>=2000;

