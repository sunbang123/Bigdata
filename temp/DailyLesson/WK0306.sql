--사원테이블에서 사원번호와 사원이름을 합쳐서 출력하라
SELECT EMPNO ||'의 '|| ENAME -- 합침!
FROM EMP;

SELECT * FROM EMP;
--COMM이 널이 아닌 값만 출력하라!
SELECT *
FROM EMP
